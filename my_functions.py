def add_numbers(a,b):
    return a + b

def convert_temp(temp):
    return (temp * 9/5) + 32

def find_average(a,b,c):
    return (a+b+c)/3

def is_even(number):
    return number%2 == 0

def   calculate_discount(price, discount_percentage):
    Nprice= (price)*(discount_percentage/100)
    mprice = price - Nprice
    return mprice
